import React from "react";

const setHTML = (data) => {
  return <div dangerouslySetInnerHTML={{ __html: data }} />;
}

const getNodeId = (str) => {
  return str.split("/").pop();
}

const urlify = (name) => {
  return name.toLowerCase().replace(" ", "-");
}

export { setHTML, getNodeId, urlify };
    
